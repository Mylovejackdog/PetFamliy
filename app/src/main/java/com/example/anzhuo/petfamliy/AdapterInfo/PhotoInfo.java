public class PhotoInfo {
    private int newHead;
    private String newName;
    private String newTime;
    private String newPhoto;
    private String newContent;
    private String praiseNum;
    private String commentNum;
    private String transmitNum;

    public PhotoInfo() {
    }

    public int getNewHead() {
        return newHead;
    }

    public void setNewHead(int newHead) {
        this.newHead = newHead;
    }

    public String getNewName() {
        return newName;
    }

    public void setNewName(String newName) {
        this.newName = newName;
    }

    public String getNewTime() {
        return newTime;
    }

    public void setNewTime(String newTime) {
        this.newTime = newTime;
    }

    public String getNewPhoto() {
        return newPhoto;
    }

    public void setNewPhoto(String newPhoto) {
        this.newPhoto = newPhoto;
    }

    public String getNewContent() {
        return newContent;
    }

    public void setNewContent(String newContent) {
        this.newContent = newContent;
    }

    public String getPraiseNum() {
        return praiseNum;
    }

    public void setPraiseNum(String praiseNum) {
        this.praiseNum = praiseNum;
    }

    public String getCommentNum() {
        return commentNum;
    }

    public void setCommentNum(String commentNum) {
        this.commentNum = commentNum;
    }

    public String getTransmitNum() {
        return transmitNum;
    }

    public void setTransmitNum(String transmitNum) {
        this.transmitNum = transmitNum;
    }
}
